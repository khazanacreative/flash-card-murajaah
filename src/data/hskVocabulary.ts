export type HSKLevel = 1 | 2 | 3 | 4 | 5;

export interface Vocabulary {
  id: string;
  hanzi: string;
  pinyin: string;
  meaning: string;
  english?: string;
  level: HSKLevel;
  kalimat?: string;
  pinyinKalimat?: string;
  artiKalimat?: string;
}

type RawEntry = [string, string, string, string, string, string, string];

function buildVocab(raw: RawEntry[], level: HSKLevel): Vocabulary[] {
  return raw.map((r, i) => ({
    id: `hsk${level}_${String(i + 1).padStart(3, '0')}`,
    hanzi: r[0],
    pinyin: r[1],
    meaning: r[2],
    english: r[3] || undefined,
    level,
    kalimat: r[4] || undefined,
    pinyinKalimat: r[5] || undefined,
    artiKalimat: r[6] || undefined,
  }));
}

