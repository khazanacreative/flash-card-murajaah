export function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

/**
 * Prepare vocabulary list for session
 * No limit - uses ALL words from each level
 * All levels: equal distribution from each available level
 */
export function prepareVocabularyList(level: HSKLevel | 'all'): Vocabulary[] {
  if (level === 'all') {
    const allShuffled = shuffleArray(vocabularyDatabase);
    return allShuffled;
  } else {
    return shuffleArray(getVocabularyByLevel(level));
  }
}
